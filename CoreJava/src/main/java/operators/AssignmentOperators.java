package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a = 10;
		System.out.println("A = "+a);//10
		a +=5;// a = a+5=10+5=15
		System.out.println("A = "+a);//15
		a -=3;// a =a-3=15-3=12
		System.out.println("A = "+a);//12
		a *=2;// a = a*2=12*2=24
		System.out.println("A = "+a);//24
		a /=4;//a = a/4=24/4
		System.out.println("A = "+a);
		a %=5;
		System.out.println("A = "+a);
		
		

	}

}
